    private void CustNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CustNameActionPerformed
        // TODO add your handling code here:
        String custname=CustName.getText();
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            String query="select * from newcustomer where CustName='"+custname+"'";
            ResultSet rs=st.executeQuery(query);
            if(rs.next())
            {
                Contact.setText(rs.getString(2));
                Email.setText(rs.getString(3));
                Address.setText(rs.getString(4));
                Email.setEditable(false);
                Address.setEditable(false);
            }
            else{
                JOptionPane.showMessageDialog(null,"Please Register");
            }
        }
        catch(Exception e)
        {
                JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_CustNameActionPerformed

